package com.event.model;

public class Sponser {
	
	public Sponser(String sponserName, String sponserAddress,
			String sponserPANCardNumber, float sponserAmount) {
		super();
		this.sponserName = sponserName;
		this.sponserAddress = sponserAddress;
		this.sponserPANCardNumber = sponserPANCardNumber;
		this.sponserAmount = sponserAmount;
	}
	private String sponserName;
	private String sponserAddress;
	private String sponserPANCardNumber;
	private float sponserAmount;
	

}
